// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

async function seed() {
  console.log('🌱  Starting seed…\n');

  // --- Personas ---
  for (const persona of PERSONAS) {
    const result = await db
      .insert(schema.personas)
      .values(persona)
      .onConflictDoNothing({ target: schema.personas.id })
      .returning({ id: schema.personas.id });

    if (result.length > 0) {
      console.log(`  ✅  Persona inserted: ${persona.id} (${persona.full_name})`);
    } else {
      console.log(`  ⏭️   Persona skipped (already exists): ${persona.id}`);
    }
  }

  console.log('');

  // --- Policies ---
  for (const policy of POLICIES) {
    const result = await db
      .insert(schema.policies)
      .values(policy)
      .onConflictDoNothing({ target: schema.policies.policy_no })
      .returning({
        id: schema.policies.id,
        policy_no: schema.policies.policy_no,
      });

    if (result.length > 0) {
      console.log(
        `  ✅  Policy inserted: ${policy.policy_no} → persona ${policy.persona_id}`,
      );
    } else {
      console.log(
        `  ⏭️   Policy skipped (already exists): ${policy.policy_no}`,
      );
    }
  }

  console.log('\n✅  Seed complete.\n');
  await client.end();
}

seed().catch((err) => {
  console.error('❌  Seed failed:', err);
  process.exit(1);
});
