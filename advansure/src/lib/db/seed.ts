/**
 * Seed script — 3 demo personas + their policies.
 *
 * Idempotent: uses onConflictDoNothing() so re-running is safe.
 *
 * Usage (recommended — tsx auto-loads .env.local):
 *   npx tsx --env-file=.env.local src/lib/db/seed.ts
 *
 * Alternative (if tsx >= 4.x):
 *   DATABASE_URL="postgresql://..." npx tsx src/lib/db/seed.ts
 *
 * Requires: npm install -D dotenv tsx  (if not already present)
 */

import { drizzle } from 'drizzle-orm/postgres-js';
import postgres from 'postgres';

import * as schema from './schema';
import type { NewPersona, NewPolicy } from './schema';

